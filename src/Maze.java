    public void centerPacman(PacMan pacMan) {
        pacMan.move(Constants.WINDOW_WIDTH / 2, Constants.WINDOW_HEIGHT / 2);
    }

    // Calculate if Pacman can make his next move without running into a wall. Note that because GCompound.contains() only
    // checks for the point at the top left of the circle, MOVE_RIGHT and MOVE_DOWN add the size of of rectangle enclosing
    // the circle to check the whole circle,instead of just one point.
    public boolean canMove(PacMan pacMan, KeyEvent e) {
        double endX = pacMan.getX();
        double endY = pacMan.getY();

        switch (e.getKeyCode()) {
            case Constants.MOVE_UP:
                endY -= Constants.MOVE_DISTANCE;
                break;
            case Constants.MOVE_LEFT:
                endX -= Constants.MOVE_DISTANCE;
                break;
            case Constants.MOVE_DOWN:
                endY += Constants.MOVE_DISTANCE;
                return !this.contains(endX, endY + pacMan.getHeight());
            case Constants.MOVE_RIGHT:
                endX += Constants.MOVE_DISTANCE;
                return !this.contains(endX + pacMan.getWidth(), endY);
        }

        return !this.contains(endX, endY);
    }
